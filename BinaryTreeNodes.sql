SELECT N, 'Leaf' AS NodeType 
FROM BST
WHERE N NOT IN(
    SELECT DISTINCT P 
    FROM BST 
    WHERE P IS NOT NULL
)

UNION 

SELECT N, 'Inner' AS NodeType 
FROM BST
WHERE P IS NOT NULL
AND N IN  (
    SELECT DISTINCT P 
    FROM BST
)

UNION 

SELECT N, 'Root' AS NodeType
FROM BST 
WHERE P IS NULL


ORDER BY N ASC;
